class Calculator(object):

    # 加算
    def add(self, num1, num2):
        return num1 + num2

    # 減算
    def sub(self, num1, num2):
        return num1 - num2

    # 乗算
    def mul(self, num1, num2):
        return num1 * num2

    # 除算
    def div(self, num1, num2):
        return num1 // num2
